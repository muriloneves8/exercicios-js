let num = parseInt(prompt("Digite um numero"));

if (num <= 10){
    console.log("Este numero é menor ou igual a 10")
}else if (num <= 20){
     console.log("Este número é maior que 10 ou menor e igual que 20")
}else if (num <= 30){
    console.log("Este é maior que 20 ou menor e igual a 30")
}else if (num > 30){
    console.log("Este número é maior que 30")
}